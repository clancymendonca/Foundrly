import { writeFileSync } from 'node:fs'

import { loadLocalEnv } from './load-local-env.mjs'

loadLocalEnv()

const config = {
  apiKey: process.env.NEXT_PUBLIC_FIREBASE_API_KEY,
  authDomain: process.env.NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN,
  projectId: process.env.NEXT_PUBLIC_FIREBASE_PROJECT_ID,
  messagingSenderId: process.env.NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID,
  appId: process.env.NEXT_PUBLIC_FIREBASE_APP_ID,
}

const missing = Object.entries(config)
  .filter(([, value]) => !value)
  .map(([key]) => key)

if (missing.length) {
  console.warn(
    `[firebase-config] Skipping generation; missing env: ${missing.join(', ')}`
  )
  process.exit(0)
}

const content = `// Generated by scripts/generate-firebase-config.mjs — do not edit by hand.
self.__FIREBASE_CONFIG = ${JSON.stringify(config, null, 2)};
`

writeFileSync('public/firebase-config.js', content)
console.log('[firebase-config] Wrote public/firebase-config.js')
